# Assigning values to variables a, b, and c
a, b, c = 1, 2, 3
print(a)  # Output: 1
print(b)  # Output: 2
print(c)  # Output: 3

# Assigning a new value to variable d
d = 10
print(d)  # Output: 10

# Reassigning a new value to variable d
d = 5
print(d)  # Output: 5

# Deleting variable d
del d
print(d)  # Raises an error since d is no longer defined