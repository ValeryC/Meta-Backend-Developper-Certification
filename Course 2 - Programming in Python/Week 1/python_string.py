a = 'a single line'  # Assigning a string to variable 'a'
print(a)  # Printing the value of 'a'

b = "a double line"  # Assigning a string to variable 'b'
print(b)  # Printing the value of 'b'

print(a + b)  # Concatenating 'a' and 'b' strings and printing the result

