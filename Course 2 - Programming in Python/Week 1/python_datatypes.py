a = 10.0  # Assigns the float value 10.0 to variable a
print(type(a))  # Prints the type of variable a, which is <class 'float'>

b = 2.3  # Assigns the float value 2.3 to variable b
print(type(b))  # Prints the type of variable b, which is <class 'float'>

c = 'Hello'  # Assigns the string 'Hello' to variable c
print(type(c))  # Prints the type of variable c, which is <class 'str'>

d = [1, 2, 3, 4, 5]  # Assigns the list [1, 2, 3, 4, 5] to variable d
print(type(d))  # Prints the type of variable d, which is <class 'list'>